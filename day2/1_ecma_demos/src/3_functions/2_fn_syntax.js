// Function Declaration
function hello1() {
    return "Hello World from hello1"
}

console.log(hello1());

// Function Expression
const hello2 = function () {
    return "Hello World from hello2"
}

console.log(hello2());

// Arrow Syntax - Multiline
const hello3 = () => {
    return "Hello World from hello3"
}

console.log(hello3());


// Arrow Syntax - Singleline
const hello4 = () => "Hello World from hello4";

console.log(hello4());
